from services.api_service import send_message
from keyboards.user_keyboards import main_keyboard
from config import PRICE_PER_DAY


def handle_text_commands(message):
    chat_id = message["chat"]["id"]
    text = message.get("text", "")

    # =========================
    # قیمت تبلیغات
    # =========================

    if text == "💵 قیمت تبلیغات":

        send_message(
            chat_id,
            f"""
💵 تعرفه تبلیغات

📅 هر روز تبلیغ: {PRICE_PER_DAY:,} تومان

📌 مثال:
• ۳ روز = {PRICE_PER_DAY * 3:,} تومان
• ۷ روز = {PRICE_PER_DAY * 7:,} تومان
• ۳۰ روز = {PRICE_PER_DAY * 30:,} تومان

برای ثبت تبلیغ روی دکمه 📢 ثبت تبلیغات کلیک کنید.
""",
            main_keyboard()
        )

        return True

    # =========================
    # راهنما
    # =========================

    if text == "📖 راهنما":

        send_message(
            chat_id,
            """
*📖 راهنمای استفاده از ربات*
«فوتبال‌تایمز | 𝙁𝙤𝙤𝙩𝙗𝙖𝙡𝙡 𝙏𝙞𝙢𝙚𝙨»

برای استفاده صحیح از خدمات، لطفاً توضیحات هر بخش را مطالعه فرمایید:

*📢 ثبت تبلیغات* 
از طریق این بخش می‌توانید درخواست تبلیغ خود را ثبت نمایید.  
پس از تکمیل اطلاعات و ارسال محتوا، درخواست شما بررسی شده و نتیجه از طریق ربات اعلام خواهد شد.

*💵 قیمت تبلیغات*
در این قسمت می‌توانید تعرفه‌ها و شرایط مالی مربوط به تبلیغات را مشاهده نمایید.  
لطفاً پیش از ثبت سفارش، قیمت‌ها را به‌دقت بررسی فرمایید.

*💬 پشتیبانی*  
در صورت وجود هرگونه سؤال، ابهام یا نیاز به راهنمایی، از این بخش استفاده کنید.  
پیام خود را ارسال نمایید تا مشاوران ما در اسرع وقت پاسخگو باشند.

*📖 راهنما*  
این بخش شامل توضیحات کلی درباره نحوه استفاده از ربات، روند ثبت درخواست و قوانین مربوطه می‌باشد.  
مطالعه این قسمت پیش از هرگونه اقدام توصیه می‌شود.
""",
            main_keyboard()
        )

        return True

    return False
